export function lookupCommutationFactor(scheme: '1995' | '2008' | '2015', ageYears: number, ageMonths: number): number {
  if (scheme === '2008') return COMMUTATION_2008;
  if (scheme === '2015') return COMMUTATION_2015;
  // 1995 Section: detailed lookup
  const floorAge = Math.floor(ageYears);
  if (floorAge < 49) return COMMUTATION_1995_SCOTLAND[49]?.[0] ?? 22.3;
  if (floorAge >= 75) return COMMUTATION_1995_SCOTLAND[75]?.[0] ?? 10.1;
  const row = COMMUTATION_1995_SCOTLAND[floorAge];
  if (!row) return 12;
  const month = Math.min(Math.max(0, Math.floor(ageMonths)), 11);
  return row[month] ?? row[0] ?? 12;
}