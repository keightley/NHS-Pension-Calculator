/**
 * Look up a factor from a table by age/years and completed months.
 * Returns 1.0 if the exact entry is not found (i.e., at NPA).
 */
export function lookupFactor(
  table: Record<number, Record<number, number>>,
  yearsOrAge: number,
  completedMonths: number
): number {
  const floorYears = Math.floor(yearsOrAge);
  const row = table[floorYears];
  if (!row) return 1.0;
  const month = Math.min(Math.max(0, Math.floor(completedMonths)), 11);
  return row[month] ?? row[0] ?? 1.0;
}
